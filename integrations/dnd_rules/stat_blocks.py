def seed_monsters(session: Session) -> int:
    """Seed SRD monsters into the database if the table is empty.

    Args:
        session: Active database session.

    Returns:
        Number of monsters inserted (0 if table was already populated).
    """
    if MonsterRepo.count(session) > 0:
        return 0

    inserted = 0
    for data in _SRD_MONSTERS:
        monster_data = dict(data)
        # Coerce enum string fields
        monster_data.setdefault("source", "SRD")
        monster_data.setdefault("is_custom", False)
        # Convert list fields to expected types (damage lists stored as strings)
        monster_data["damage_resistances"] = monster_data.get("damage_resistances", [])
        monster_data["damage_immunities"] = monster_data.get("damage_immunities", [])

        from domain.monster import MonsterStatBlockCreate  # local import avoids circular

        try:
            payload = MonsterStatBlockCreate(**monster_data)
            MonsterRepo.create(session, payload)
            inserted += 1
        except Exception:
            # Skip malformed entries; never fail the whole seed
            pass

    return inserted
